package com.zhan.cyberdb.storage;

/**
 * 元组（Tuple）—— 数据库表中的一行数据。
 * <p>
 * 元组是关系型数据库中最基本的数据承载单元，对应 SQL 表中的一条记录（Row）。
 * 在 CyberDB 存储引擎中，元组的物理形式是一段连续的字节数组，其内部结构由上层
 * 表结构定义（Schema）决定，但本类不关心字段的具体解析方式，仅作为字节数组的容器
 * 在存储层、执行器层和索引层之间传递。
 * </p>
 *
 * <h2>设计哲学</h2>
 * <p>
 * {@code Tuple} 被设计为一个<b>不可变的值对象（Value Object）</b>：
 * <ul>
 *   <li>一旦构造，其内部的字节数组便不可更改（没有公开的 setter）。</li>
 *   <li>任何对元组内容的修改都应通过创建新的 {@code Tuple} 实例来完成，
 *       这有助于在并发和事务处理中维护数据一致性。</li>
 *   <li>本类不提供对字段的解析方法（如 {@code getInt(offset)} 等），
 *       以保持其在存储引擎各层之间的通用性。字段解析由上层执行器或编解码器负责。</li>
 * </ul>
 * </p>
 *
 * <h2>在系统中的流转路径</h2>
 * <ol>
 *   <li><b>从磁盘读取</b>：{@link TablePage#getTuple(int)} 从分槽页中提取字节数组，
 *       构造一个 {@code Tuple} 对象返回给执行器。</li>
 *   <li><b>在算子间传递</b>：执行器（如 {@code SeqScanExecutor}、{@code FilterExecutor}）
 *       以 {@code Tuple} 为单位进行流水线处理，进行过滤、投影、连接等操作。</li>
 *   <li><b>写入索引</b>：在创建索引时，元组中的键字段被提取出来，与元组的物理位置
 *       （如页面 ID 和槽位号）一起插入 B+ 树。</li>
 *   <li><b>插入磁盘</b>：通过 {@link TablePage#insertTuple(Tuple)} 将元组的字节数组
 *       写入分槽页的数据区，完成持久化。</li>
 * </ol>
 *
 * <h2>内存占用</h2>
 * <p>
 * 每个 {@code Tuple} 对象仅持有对字节数组的引用，内存开销极小。字节数组的实际大小
 * 取决于表结构定义和各字段的实际值，可能从几个字节到接近页面大小（4KB）不等。
 * 在生产环境中，通常会实现元组缓存或对象池来减少频繁创建 {@code Tuple} 带来的 GC 压力。
 * </p>
 *
 * @author Zhan
 * @version 1.0
 * @see TablePage
 * @see com.zhan.cyberdb.execution.Executor
 */
public class Tuple {

    /**
     * 元组的真实数据内容，以字节数组形式存储。
     * <p>
     * 该字节数组的布局由表的 Schema 定义决定。通常采用“首部定长字段 + 变长字段偏移数组 +
     * 变长数据区”的经典行格式，但本类不做任何假设，仅原样保存。
     * </p>
     * <p>
     * <b>不可变性</b>：此字段为 {@code private final}（实际上未显式声明 final，
     * 但无 setter 且不对外暴露修改能力），确保元组在构造后内容不变。
     * 若需修改，调用者应构造新的 {@code Tuple} 实例。
     * </p>
     */
    private byte[] data;

    /**
     * 构造一个元组对象，包装给定的字节数组。
     * <p>
     * 为了提高性能并遵循零拷贝思想，构造时<b>不进行数组拷贝</b>，直接持有传入的引用。
     * 因此调用者必须确保传入的字节数组在元组的生命周期内不会被外部修改。
     * 通常该数组来自磁盘读取或执行器生成，天然符合此约定。
     * </p>
     *
     * @param data 表示该行数据的字节数组。不能为 {@code null}，但可以是长度为 0 的空数组
     *             （例如某些系统表可能包含空行）。
     */
    public Tuple(byte[] data) {
        this.data = data;
    }

    /**
     * 获取元组的底层字节数组。
     * <p>
     * 注意：返回的是内部数组的直接引用，而非防御性拷贝。这是有意为之，以避免不必要的内存分配
     * 和拷贝开销。调用者<b>不得</b>修改返回的数组内容，否则将破坏元组的不可变性约定，
     * 并可能导致并发环境下的数据不一致问题。
     * </p>
     *
     * @return 包含元组数据的字节数组。绝不返回 {@code null}。
     */
    public byte[] getData() {
        return data;
    }

    /**
     * 获取元组所占用的字节长度。
     * <p>
     * 该长度等于构造时传入的字节数组的 {@code length} 属性。此值在元组插入页面时
     * 用于计算所需空间，以及在扫描时确定需要从页面读取多少字节。
     * </p>
     *
     * @return 元组数据的字节数，非负整数。
     */
    public int getLength() {
        return data.length;
    }
}